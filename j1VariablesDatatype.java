class j1VariableDatatype{
    public static void main(String args[]){

        /*
         * Primitive Datatype
         *      - Non Numeric Type -> boolean, char
         *      - Numeric Type
         *          - Interger -> int, byte, short, long
         *          - Float - float, double
         * Non-Primitive Datatype -> String, Array, etc
         * 
         * Note: Default for Floating point Numbers in Double
         */

        int num1 = 10;
        int num2 = 20;

        float num3 = 5.89f;
        double num4 = 17.2234;
        
        char ch = 'a';

        boolean b1 = true;
        
        System.out.println(num1 + ": " + num2 + ": " + num3 + ": " + num4 + ": " + ch + ": " + b1);
    }
}